package com.iwakfu.model;

public class ItemDetail {
	private String name;
	private int level;
	private int ap;
	private int hp;
	private int mp;
	private int wp;
	private int initivate;
	private int dodge;
	private int lock;
	private int backstab;
	
	
	public int getInitivate() {
		return initivate;
	}

	public void setInitivate(int initivate) {
		this.initivate = initivate;
	}

	public int getDodge() {
		return dodge;
	}

	public void setDodge(int dodge) {
		this.dodge = dodge;
	}

	public int getLock() {
		return lock;
	}

	public void setLock(int lock) {
		this.lock = lock;
	}

	public int getBackstab() {
		return backstab;
	}

	public void setBackstab(int backstab) {
		this.backstab = backstab;
	}

	public int getMp() {
		return mp;
	}

	public void setMp(int mp) {
		this.mp = mp;
	}

	public int getWp() {
		return wp;
	}

	public void setWp(int wp) {
		this.wp = wp;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getAp() {
		return ap;
	}

	public void setAp(int ap) {
		this.ap = ap;
	}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}

}
